package model;

public class Aluno {
	private String nome;
	private String ra;
	
	public void setNomeA(String nome) {
		this.nome = nome;
	}
	public void setRA(String ra) {
		this.ra = ra;
	}
	public String getNomeA() {
		return nome;
	}
	public String getRA() {
		return ra;
	}
	
	
}
